#include "libft.h"

int	ft_atoi(const char *s)
{
	int	signe;
	int	res;
	int	i;

	signe = 1;
	res = 0;
	i = 0;
	while ((s[i] >= 9 && s[i] <= 13) || \
		s[i] == 32)
		i++;
	while (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			signe *= (-1);
		if (s[i + 1] == '+' || s[i + 1] == '-')
			return (0);
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res * signe);
}
